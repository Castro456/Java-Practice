
public class math_class {
	public static void main(String[] args) {
		double a = 3.14;
		double b = -5;
		double c = Math.max(a, b); //says which is big number
		double d = Math.abs(b); //gives absolute values
		double e = Math.sqrt(a);
		double f = Math.ceil(a); //rounds up the value  where "floor" rounds down
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
	}
}
