
public class overloaded_con {
	
	String book1;
	String book2;
	String book3;
	
	overloaded_con(String book1,
	String book2){
		this.book1 = book1;
		this.book2 = book2;
	} 
	
	overloaded_con(String book1,
	String book2,
	String book3){
		this.book1 = book1;
		this.book2 = book2;
		this.book3 = book3;
	}
}
